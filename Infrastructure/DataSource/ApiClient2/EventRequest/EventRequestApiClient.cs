
using System.Net.Http;
using System.Threading.Tasks;
using Infrastructure.Nswag;
using Infrastructure.Share.Invoker;
using AutoMapper;
using Shared.Interfaces;
using Infrastructure.DataSource.ApiClientBase;
using Infrastructure.DataSource.ApiClientFactory;
using Infrastructure.Share.Invoker;
using Microsoft.Extensions.Configuration;
namespace Infrastructure.DataSource.ApiClient2;


 public  class EventRequestApiClient : BuildApiClient<EventRequestClient>  , IEventRequestApiClient {

  
    public EventRequestApiClient(ClientFactory clientFactory, IMapper mapper,IApiInvoker apiInvoker) : base(clientFactory, mapper, apiInvoker){

    }
                

    public   async Task<ICollection<EventRequestOutputVM>> GetEventRequestsAsync(CancellationToken cancellationToken)
    {
    
         return   await apiInvoker.InvokeAsync(async () => {
            var client = await GetApiClient();
             return    await client.GetEventRequestsAsync(cancellationToken);
        });
                
    }


    public   async Task<EventRequestOutputVM> GetEventRequestAsync(string id, CancellationToken cancellationToken)
    {
    
         return   await apiInvoker.InvokeAsync(async () => {
            var client = await GetApiClient();
             return    await client.GetEventRequestAsync(id, cancellationToken);
        });
                
    }


    public   async Task DeleteEventRequestAsync(string id, CancellationToken cancellationToken)
    {
    
         await apiInvoker.InvokeAsync(async () => {
            var client = await GetApiClient();
              await client.DeleteEventRequestAsync(id, cancellationToken);
        });
                
    }


    public   async Task<int> CountEventRequestAsync(CancellationToken cancellationToken)
    {
    
         return   await apiInvoker.InvokeAsync(async () => {
            var client = await GetApiClient();
             return    await client.CountEventRequestAsync(cancellationToken);
        });
                
    }


}

